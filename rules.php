    <p>The goal of Ice Insignia is simple, defeat your foe in the field of battle. <br><br>
    To do this you have several different fighters to work with. Try and build a team that works well together!</p>
    <h3>Basics</h3>
    <ul>
      <li>You need two people to play this game</li>
      <li>Login/Register then select your 5 pieces and the items they will carry into battle</li>
      <li>Select the battle field and get ready for war</li>
      <li>One "round" is composed of Player1's "turn" followed by Player2's "turn" </li>
      <li>Once Player1 has moved every piece's move count to 0, it is Player2's turn</li>
      <li>Select a piece you wish to move on your team by clicking on it.</li>
      <li>Once clicked, you can move the selected piece with the arrow keys or user a spell/attack/item</li>
      <li>Each piece has 5 or 6 moves to use per turn each arrow key movement counts as one of those moves</li>
      <li>When the move count of a piece hits 0, that piece cannot move again until the next turn</li>
      <li>When you "pass", use an attack, or use a spell, that piece's move count will be set to 0</li>
      <li>the "pass" option end's that piece's turn</li>
      <li>To target another piece, select your piece and its attack/spell and then click on the piece you target.</li>
      <li>Watch out! This is real war out here! Do not attack your own team!</li>
      </ul>
      
      <h3>Stats</h3>
      <ul>
      <li>Health - how many life points a piece has</li>      
      <li>Strength - determines how strong a piece's close-range/physical attacks will be</li>
      <li>Defense - determines how well a piece can take attack from the enemy</li>
      <li>Intellect - determines how powerful a piece's long range/magic attacks will be</li>
      <li>Movement - How many moves a piece can make in one turn</li>
      <li>Range - how far from its current location a piece can attack. </li>
      </ul>
      
      <h3>Items</h3>
      <ul>
      <li>Nikes - permanently increase the users movement by 1</li>
      <li>Ring - increase user's strength by 30 </li>
      <li>Book of Mormon - Increase user's intellect by 50</li>
      <li>Health Pot - increase user's health by 60 </li>
      <li>Chainmail - increase user's defense by 45</li>
      </ul>


